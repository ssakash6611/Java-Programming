public class Square
{
  double h,w,a;
   public void setHeight(double x)
  {
    h=x;
  }
   public double getHeight()
  {
    return h;
  }
   public void setWidth (double p)
  {
    w=p;
  }
  public double getWidth ()
  {
    return w;
  }
  public double getArea ()
  {
    a=h*w;
    return a;
  }
}

    
    
  




